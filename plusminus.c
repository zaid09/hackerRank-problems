#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main() {

   int n;float sum=0.0, sum2=0.0;
    scanf("%d", &n);
    int a[n];
    int i;
    for(i=0;i<n;i++)
        {
        scanf("%d", &a[i]);
    }
    for(i=0;i<n;i++)
        {
        if(a[i]<0)
            sum++;
        if(a[i]>0)
            sum2++;
       
    }
    printf("%.3f", (sum2/n));
    printf("\n");
    printf("%.3f",(sum/n));
    printf("\n");
    printf("%.3f", ((n-(sum+sum2))/n));
    printf("\n");
    return 0;
}
